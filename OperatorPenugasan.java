
public class OperatorPenugasan {
    public static void main(String[] args) {
        double mynumber=50;
        System.out.println("Awalnya variabel mynumber bernilai " + mynumber);
        
        mynumber +=50;
        System.out.println("Setelah ditambahkan dengan 50, maka variabel mynumber bernilai " + mynumber);
        
        mynumber -=22;
        System.out.println("Setelah dikurangi dengan 22, maka variabel mynumber bernilai " + mynumber);
        
        mynumber *=2;
        System.out.println("Setelah dikalikan dengan 2, maka variabel mynumber bernilai " + mynumber);
        
        mynumber /=4;
        System.out.println("Setelah dibagi dengan 4, maka variabel mynumber bernilai " + mynumber);
        
        mynumber %=2;
        System.out.println("Setelah dimodulus dengan 2, maka variabel mynumber bernilai " + mynumber);
    }
}
